class SimpleCals:

    def add(self, value1, value2):
        return value1+value2

    def subtract(self, value1, value2 ):
        return value1-value2
    def multiply(self, value1, value2 ):
        return value1*value2
    def divide(self, value1, value2 ):
        return value1/value2


# create a funciton for DOB
# function for %
# another func for cm to m

    def DOBcalc(self, value1, value2):
        return f"{value1}/{value2}"

    def remainders(self, value1, value2):
        return value1%value2

    def cmtom(self, value):
        return value/100